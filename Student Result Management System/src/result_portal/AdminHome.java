    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String course = jComboBox1.getSelectedItem().toString();
        String branch = jComboBox2.getSelectedItem().toString();
        String roll = jTextField1.getText();
        String name = jTextField2.getText();
        String gender = jComboBox3.getSelectedItem().toString();
        String fatherName = jTextField3.getText();
        
        if (roll == null || roll.replaceAll("\\s", "").equals("")) {
            JOptionPane.showMessageDialog(null, "Enter a valid roll number");
            return;
        }
        
        if (name == null || name.replaceAll("\\s", "").equals("")) {
            JOptionPane.showMessageDialog(null, "Enter student name");
            return;
        }
        
        //SQL
        String user = "root";
        String pwd = "Klowista5863@";
        String db = "srms";
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/" + db + "?user=" + user + "&password=" + pwd);
            Statement st = con.createStatement();
            st.executeUpdate("INSERT INTO student(`Roll No`, Course, Branch, Name, Gender, `Father's Name`) VALUES('" + roll + "', '" + course + "', '" + branch + "', '" + name + "', '" + gender + "', '" + fatherName + "')");
            JOptionPane.showMessageDialog(null, "Data saved successfully");
            this.setVisible(false);
            new AdminHome().setVisible(true);
            
            st.close();
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

