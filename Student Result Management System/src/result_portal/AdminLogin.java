    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username_str = username.getText();
        String password_str = password.getText();
        
        //SQL
        String user = "root";
        String pwd = "Klowista5863@";
        String db = "srms";
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/" + db + "?user=" + user + "&password=" + pwd);
            PreparedStatement stmt = con.prepareStatement("SELECT username FROM admin_credentials WHERE username = ? AND password = ?");
            stmt.setString(1, username_str);
            stmt.setString(2, password_str);
            
            ResultSet rs = stmt.executeQuery();
            if (rs.next()) {
                this.setVisible(false);
                new AdminHome().setVisible(true);
            }
            else {
                JOptionPane.showMessageDialog(null, "Incorrect username or password");
            }
            
            rs.close();
            stmt.close();
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

